class R18UnicodeParser extends AbstractUnicodeParser {
  filter(str: string) {
    let result = str
    for(let index = 0, length = R18_ENTRY.length; index < length; index ++) {
      const r18 = R18_ENTRY[index]
      result = result.replaceAll(r18.key, r18.value)
    }
    return result
  }
}

class EmojiParser extends AbstractUnicodeParser {
  filter(str: string) {
    const regex = /:([a-zA-Z_]{1,}):/g
    const list = str.match(regex) ?? []
    let result = str
    for(let index = 0, length = list.length; index < length; index ++) {
      const emoji = list[index].substr(1, list[index].length - 2)
      if (EMOJI_ENTRY[emoji]) {
        result = result.replace(`:${emoji}:`, EMOJI_ENTRY[emoji])
      } else if (EMOJI_ENTRY[emoji + "_face"]) {
        result = result.replace(`:${emoji}:`, EMOJI_ENTRY[emoji + "_face"])
      }
    }
    return result
  }
}

